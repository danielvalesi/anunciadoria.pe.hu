Por favor, leia cuidadosamente.

Sabemos que normalmente esse documento tem 50 páginas redundantes, rebuscadas e acima de tudo chatas que nos deixam mais confusos do que antes de termos lido.

Fizemos algo diferente, de linguagem simples e objetiva para facilitar o entendimento.

O que somos e fazemos:

- Plataforma online que divulga espaços midiáticos, seja ele qual for, para empresas/pessoas contratantes.
- Estamos constantemente incrementando a nossa ferramenta de maneira a sempre ser o mais útil e completa para que esses dois universos se conversem.
- Temos um banco de dados sigiloso e protegido que permite acesso apenas a um mesmo CNPJ ou CPF e isso nos permite regular e punir qualquer usuário que haja de má fé ou fora das políticas da Plataforma.

O que não somos e não fazemos:

- Não somos intermediadores financeiros e não nos responsabilizamos pelas negociações entre as partes, pois entendemos que ninguém melhor do que os próprios usuários para entender qual a sua necessidade e as condições de que dispõe.
- Não avaliamos usuários, mas permitimos e incentivamos que quem mais entende do assunto avalie: você!
- Com exceção da Plataforma, não possuímos espaço midiático algum, pois não faz sentido concorrer com nossos próprios usuários.

Mas então não nos preocupamos com a segurança das transações entre nossos usuários?

Vamos exercitar para ficar mais fácil o entendimento de como efetuar negociações mais seguras e transparentes entre os usuários.

Quando estou oferecendo um anúncio:

- Todos os anúncios são de responsabilidade dos próprios anunciantes, pois acreditamos que o trabalho sério e honesto trás muito mais resultado do que aquele "jeitinho".
- Faça um anúncio de forma clara e transparente de forma que não gere dúvida sobre o serviço oferecido.
- Avalie também quem está negociando pelo histórico de avaliações e caso seja um usuário novo, procure pedir referências de prestadores de serviço.

Quando estou contratando anúncio:

- Lembre-se de que seu nome é sua credibilidade e por isso é muito importante honrar com a contratação de um espaço pela plataforma como em qualquer outro negócio que fizer.
- Deixe claro a sua necessidade e negocie para pagar amarrado a veiculação da publicidade se possível.
- Procure avaliar as referências sobre quem está oferecendo o anúncio e se for um usuário novo tente pegar referência de quem já o contratou.

O não cumprimento das boas práticas de negociação pode levar a exclusão permanente dos usuários e o bloqueio de uso pelo CNPJ ou CPF seja ele anunciante ou contratante.

Acreditamos no mundo com negociações mais simples e diretas.</textarea>
